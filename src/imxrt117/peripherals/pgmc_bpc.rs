/// BPC Authentication Control
pub mod BPC_AUTHEN_CTRL {

    /// Allow user mode access
    pub mod USER {
        /// Offset (0 bits)
        pub const offset: u32 = 0;
        /// Mask (1 bit: 1 << 0)
        pub const mask: u32 = 1 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values
        pub mod RW {

            /// 0b0: Allow only privilege mode to access basic power control registers
            pub const USER_0: u32 = 0b0;

            /// 0b1: Allow both privilege and user mode to access basic power control registers
            pub const USER_1: u32 = 0b1;
        }
    }

    /// Allow non-secure mode access
    pub mod NONSECURE {
        /// Offset (1 bits)
        pub const offset: u32 = 1;
        /// Mask (1 bit: 1 << 1)
        pub const mask: u32 = 1 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values
        pub mod RW {

            /// 0b0: Allow only secure mode to access basic power control registers
            pub const NONSECURE_0: u32 = 0b0;

            /// 0b1: Allow both secure and non-secure mode to access basic power control registers
            pub const NONSECURE_1: u32 = 0b1;
        }
    }

    /// Lock NONSECURE and USER
    pub mod LOCK_SETTING {
        /// Offset (4 bits)
        pub const offset: u32 = 4;
        /// Mask (1 bit: 1 << 4)
        pub const mask: u32 = 1 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values (empty)
        pub mod RW {}
    }

    /// Domain ID white list
    pub mod WHITE_LIST {
        /// Offset (8 bits)
        pub const offset: u32 = 8;
        /// Mask (4 bits: 0b1111 << 8)
        pub const mask: u32 = 0b1111 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values (empty)
        pub mod RW {}
    }

    /// White list lock
    pub mod LOCK_LIST {
        /// Offset (12 bits)
        pub const offset: u32 = 12;
        /// Mask (1 bit: 1 << 12)
        pub const mask: u32 = 1 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values (empty)
        pub mod RW {}
    }

    /// Configuration lock
    pub mod LOCK_CFG {
        /// Offset (20 bits)
        pub const offset: u32 = 20;
        /// Mask (1 bit: 1 << 20)
        pub const mask: u32 = 1 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values (empty)
        pub mod RW {}
    }
}

/// BPC Mode
pub mod BPC_MODE {

    /// Control mode. This field is locked by AUTHEN_CTRL\[LOCK_CFG\] field.
    pub mod CTRL_MODE {
        /// Offset (0 bits)
        pub const offset: u32 = 0;
        /// Mask (2 bits: 0b11 << 0)
        pub const mask: u32 = 0b11 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values
        pub mod RW {

            /// 0b00: Not affected by any low power mode
            pub const CTRL_MODE_0: u32 = 0b00;

            /// 0b01: Controlled by CPU power mode of the domain
            pub const CTRL_MODE_1: u32 = 0b01;

            /// 0b10: Controlled by Setpoint
            pub const CTRL_MODE_2: u32 = 0b10;
        }
    }

    /// Domain assignment of the BPC
    pub mod DOMAIN_ASSIGN {
        /// Offset (4 bits)
        pub const offset: u32 = 4;
        /// Mask (2 bits: 0b11 << 4)
        pub const mask: u32 = 0b11 << offset;
        /// Read-only values (empty)
        pub mod R {}
        /// Write-only values (empty)
        pub mod W {}
        /// Read-write values
        pub mod RW {

            /// 0b00: Domain 0
            pub const d0: u32 = 0b00;

            /// 0b01: Domain 1
            pub const d1: u32 = 0b01;

            /// 0b10: Domain 2
            pub const d2: u32 = 0b10;

            /// 0b11: Domain 3
            pub const d3: u32 = 0b11;
        }
    }
}

